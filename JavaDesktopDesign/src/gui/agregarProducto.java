    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        try {
            Producto p = new Producto();
            p.setDescripcion(txtDescripcion.getText());
            p.setCategoria(cboCategoria.getSelectedItem().toString());
            p.setCantidad(Integer.parseInt(txtCantidad.getText()));
            p.setCosto(Double.parseDouble(txtCosto.getText()));
            p.setPrecio(Double.parseDouble(txtPrecio.getText()));
            //obtener fecha
            p.setFecing(jdcFecIng.getDate());
            //exito:D!!!
            p.setEstado(cboEstado.getSelectedItem().toString());
            Controlador c = new Controlador();
            c.insertarProducto(p);
            lblMensaje.setText("EXITO!!");
            lblMensaje.setForeground(Color.green);
        } catch (Exception e) {
            lblMensaje.setText("ERROR!!");
            lblMensaje.setForeground(Color.red);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed
