    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            Controlador c = new Controlador();
            List<Producto> productos = c.traerProductos();
            for (Producto producto : productos) {
                this.cboCodigo.addItem(producto.getCodigo());
            }
        } catch (Exception e) {
            System.out.println("ERROR CON LA CONEXION DE LA BASE DE DATOS");
        }
    }//GEN-LAST:event_formWindowOpened

    private void cboCodigoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCodigoItemStateChanged
        // TODO add your handling code here:
        Controlador c = new Controlador();
        Producto p = c.datosProducto(cboCodigo.getSelectedItem().toString());
        txtDescripcion.setText(p.getDescripcion());
        lblCategoria.setText("Categoria: " + p.getCategoria());
        txtCantidad.setText(String.valueOf(p.getCantidad()));
        txtCosto.setText(String.valueOf(p.getCosto()));
        txtPrecio.setText(String.valueOf(p.getPrecio()));
        lblfecing.setText("Fecha de Ingreso: " + String.valueOf(p.getFecing()));
        lblEstado.setText("Estado: " + p.getEstado());
    }//GEN-LAST:event_cboCodigoItemStateChanged

    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
        try {
            Producto p = new Producto();
            p.setCodigo(Integer.parseInt(cboCodigo.getSelectedItem().toString()));
            p.setDescripcion(txtDescripcion.getText());
            p.setCategoria(cboCategoria.getSelectedItem().toString());
            p.setCantidad(Integer.parseInt(txtCantidad.getText()));
            p.setCosto(Double.parseDouble(txtCosto.getText()));
            p.setPrecio(Double.parseDouble(txtPrecio.getText()));
            //obtener fecha
            p.setFecing(jdcFecIng.getDate());
            //exito:D!!!
            p.setEstado(cboEstado.getSelectedItem().toString());
            Controlador c = new Controlador();
            c.modificarProducto(p);
            lblMensaje.setText("EXITO!!");
            lblMensaje.setForeground(Color.green);
        } catch (Exception e) {
            lblMensaje.setText("ERROR!!");
            lblMensaje.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed
